private void generarPDF() {
    // Crear un documento
    Document documento = new Document();

    try {
        // Ruta donde se guardar√° el PDF
        String ruta = "ticket_Pago.pdf";

        // Configurar el escritor para el PDF
        PdfWriter.getInstance(documento, new FileOutputStream(ruta));

        // Abrir el documento
        documento.open();

        // Agregar contenido al PDF
        Font fontTitulo = new Font(Font.FontFamily.HELVETICA, 18, Font.BOLD);
        Font fontNormal = new Font(Font.FontFamily.HELVETICA, 12, Font.NORMAL);

        Paragraph titulo = new Paragraph("Pago de servicio", fontTitulo);
        titulo.setAlignment(Element.ALIGN_CENTER);
        documento.add(titulo);
        
        documento.add(new Paragraph("Nombre: " + txtNombre.getText(), fontNormal));
        documento.add(new Paragraph("Apellido: " + txtApellido.getText(), fontNormal));
        documento.add(new Paragraph("\nBanco: " + txtFactura.getSelectedItem(), fontNormal));
        documento.add(new Paragraph("Cantidad: " + txtCuenta.getText(), fontNormal));
        documento.add(new Paragraph("Importe: " + txtImporte.getText(), fontNormal));
        documento.add(new Paragraph("Descripci√≥n: " + txtDescripcion.getText(), fontNormal));
        documento.add(new Paragraph("\nGracias por su confianza.", fontNormal));

        // Mostrar mensaje de confirmaci√≥n
        JOptionPane.showMessageDialog(SwingUtilities.getWindowAncestor(this), 
                "PDF generado con √©xito en: " + ruta);

    } catch (DocumentException ex) {
        ex.printStackTrace();
        JOptionPane.showMessageDialog(SwingUtilities.getWindowAncestor(this), 
                "Error al generar el PDF: " + ex.getMessage());
    } catch (IOException ex) {
        ex.printStackTrace();
        JOptionPane.showMessageDialog(SwingUtilities.getWindowAncestor(this), 
                "Error al generar el PDF: " + ex.getMessage());
    } finally {
        // Cerrar el documento
        documento.close();
    }
}


